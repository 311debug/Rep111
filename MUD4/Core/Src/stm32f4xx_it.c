/**
  * @brief This function handles TIM1 break interrupt and TIM9 global interrupt.
  */
void TIM1_BRK_TIM9_IRQHandler(void)
{
  /* USER CODE BEGIN TIM1_BRK_TIM9_IRQn 0 */
	if (__HAL_TIM_GET_FLAG(&htim9, TIM_FLAG_UPDATE) &&
	        __HAL_TIM_GET_IT_SOURCE(&htim9, TIM_IT_UPDATE))
	    {
	        __HAL_TIM_CLEAR_IT(&htim9, TIM_IT_UPDATE);
	        uint32_t period;
	        uint16_t duty =g_duty_percent;           // пишет задача
	        if (duty < 0)   duty = 0;
	        if (duty > 100) duty = 100;


	       if (duty!=0) {period =100000000/(16*(uint32_t)duty*(100-(uint32_t)duty));}
	       else period=65535;
	        if (period>65535) period=65535;
	        uint32_t high   = (period * (uint32_t)duty) / 100;

	        if (high >= period)    high = period-1;



	       htim9.Instance->ARR=period - 1;
	       htim9.Init.Period=period - 1;
	       htim9.Instance->CCR1=high;
	        //__HAL_TIM_SET_AUTORELOAD(&htim9, period_us - 1u);
	        //__HAL_TIM_SET_COMPARE(&htim9, TIM_CHANNEL_1, high_us);
	    }


  /* USER CODE END TIM1_BRK_TIM9_IRQn 0 */
  HAL_TIM_IRQHandler(&htim9);
  /* USER CODE BEGIN TIM1_BRK_TIM9_IRQn 1 */
  /* обработать CC1 нужно для ШИМ */
     if (__HAL_TIM_GET_FLAG(&htim9, TIM_FLAG_CC1) &&
         __HAL_TIM_GET_IT_SOURCE(&htim9, TIM_IT_CC1))
     {
         __HAL_TIM_CLEAR_IT(&htim9, TIM_IT_CC1);
     }
  /* USER CODE END TIM1_BRK_TIM9_IRQn 1 */
}

